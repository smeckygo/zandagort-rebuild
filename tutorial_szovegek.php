<h3>Vissza az építkezéshez</h3>

Közben valószínűleg megépült a fűrészmalmod. Kattints balra fent a bolygó nevére, így láthatod, hogy hogyan változtak meg a termeléseid. A fűrészmalom megépülésével például nő a fatermelés, az energiafogyasztás és a munkaerőigény:
