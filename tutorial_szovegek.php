Utóbbi azt mutatja, hogy a munkásaid hány százaléka dolgozik, pontosabban hány százalékukra van szükség. Ha ez 100% alatt van, akkor érdemes újabb gyárakat építened, hogy hasznosítsd az összes embered. Ha 100% felett van, vagyis több munkásra van szüksége a gyáraidnak, mint amennyi egyáltalán van, akkor a gyárak csak részlegesen fognak üzemelni (ekkor balra a gyáraknál pirossal zárójelben látható, hogy ténylegesen hány gyár üzemel az egyes típusokból). Hogy ez a helyzet megszűnjön, növelned kell a munkások számát, és mivel ez mindig pontosan a népesség fele, ezért növelned kell a bolygó népességét.

