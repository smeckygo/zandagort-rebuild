Amint megépülnek a falvak, a lakóhelyek száma (fent látható a bolygó képétől balra) megnő, vagyis több ember fog elférni a bolygón. Először csak zárójelben látszódik az új szám, ez azt jelzi, hogy csak a következő körben lesz ennyi a lakóhelyed.

<img src=\"http://zandagort.hu/w/images/2/2e/Tutorial_13.png\" alt=\"\" class=\"tut_img\" />

És mi az a kör? Minden bolygón 15 percenként van körváltás, ekkor történik termelés, és ekkor szaporodnak az emberek, állatok, növények. A bolygó adatlapján az összefoglaló adatok (bolygó képétől balra) legalján láthatod, hogy legközelebb mikor lesz körváltás.

<img src=\"http://zandagort.hu/w/images/d/d2/Tutorial_14.png\" alt=\"\" class=\"tut_img\" />

Amint letelt egy kör, ténylegesen is megnő a lakóhelyek száma, és ha van raktáron elég ennivaló (jobbra láthatod az élelmiszerkészletet), akkor az emberek elkezdenek szaporodni, vagyis körről körre nő a népesség.

<img src=\"http://zandagort.hu/w/images/6/64/Tutorial_15.png\" alt=\"\" class=\"tut_img\" />

Most már 110 falvad van, ami 55 ezer embernek ad lakóhelyet. Idővel a népességed is felnő az eredeti 30 ezerről 55 ezerre. Viszont a kajatermelésed csak 40 ezer, vagyis minden körben több élelmiszer fogy, mint amennyi termelődik. Ezt onnan is láthatod, hogy jobbra az élelmiszernél a nettó érték negatív és pirossal van kiírva.

<img src=\"http://zandagort.hu/w/images/a/a5/Tutorial_16.png\" alt=\"\" class=\"tut_img\" />

<b>Mindig ügyelj rá, hogy ha jobbra valami piros, akkor negatív</b>, vagyis folyamatosan csökken a készlet az adott erőforrásból, és ha nem lépsz közbe időben, akkor kifogy, vagyis az azt használó gyáraid nem tudnak rendesen üzemelni. Ha az élelmiszer fogy el, akkor viszont az embereid kezdenek éhen halni, vagyis csökken a népesség egészen addig, amíg pont annyi nem lesz, mint az élelmiszertermelésed. Ahhoz, hogy ezt a helyzetet megelőzd, <b>építs 1 húsüzemet</b>. Amint megépül, a kajatermelésed 60 ezerre nő, vagyis pozitívban leszel.

Van azonban egy másik fontos tényező is, amire figyelned kell: az energia. Minden gyárad használ energiát, az erőművek (pl szélfarmok) pedig termelik. Hogy legyen elég energia a további építkezésekhez, <b>húzz fel 55 szélfarmot</b>. Később többféle erőmű közül is választhatsz majd, érdemes mindig a leghatékonyabbat választani (erről kérdezd meg a szövitársaidat).

Minden építkezéshez építőanyagokra van szükség, elsősorban fára és kőre. A játék első szakaszában ezek termelését kell jelentősen megnövelned, és persze biztosítani hozzá a kellő számú munkást (falvak, élelmiszergyárak) és a kellő mennyiségű energiát (erőművek). <b>Építs még 4 fűrészmalmot és 5 kőbányát.</b>

Előfordulhat, hogy mire ezek megépülnek, a népességed még nem nőtt meg kellően, vagyis nem lesz elég munkásod. Ezt onnan láthatod, hogy a munkaerőhelyzet 100% fölé megy, a csík piros és az van odaírva, hogy Beavatkozást igényel. Illetve onnan is, hogy a gyáraid mellett az \"aktív (üzemel)\" oszlopban zárójelben piros számok jelennek meg. Ezek mutatják, hogy mennyi gyárad üzemel tényleges a kevés munkás miatt.

<img src=\"http://zandagort.hu/w/images/b/bd/Tutorial_17.png\" alt=\"\" class=\"tut_img\" />

Avatkozz be! Ideiglenesen inaktiválj néhány fűrészmalmot vagy kőbányát. Ezt úgy teheted meg, hogy a gyár nevére kattintasz, felül beírod, hogy hányat szeretnél inaktiválni, és az Inaktivál-ra kattintasz (vagy ha még épülnek, akkor az építési listában kattints az inaktiválásra).

<img src=\"http://zandagort.hu/w/images/1/1c/Tutorial_18.png\" alt=\"\" class=\"tut_img\" />

Ekkor a gyáraid egy része inaktív lesz, vagyis nem termelnek, cserébe nem is fogyasztanak se munkaerőt, se energiát, semmit. Láthatod, hogy a gyáraid egy része átkerült az inaktív oszlopba.

<img src=\"http://zandagort.hu/w/images/1/10/Tutorial_19.png\" alt=\"\" class=\"tut_img\" />

Vagyis ha véletlenül túlépítkeztél (a népességedhez képest), ne kell lerombolnod semmit, elég inaktiválni. Később, ha már kellően nagy lett a népességed, újra aktiválhatod ezeket: beírod, hogy hányat akarsz aktiválni, és az Aktivál-ra kattintasz.

Ha mindent jól csináltál, a népesség 45 ezer fölé nő, amivel tech szintet lépsz. Ezzel egyrészt új gyárakat építhesz, másrészt tovább haladhatsz a tutorialban. Gratulálunk!

